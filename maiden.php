                    <p style="font-size: 19px;"><b>Iron Maiden</b> – британська хеві-метал група, яка у 80-х була одною
                        з найвідоміших
                        представників Нової Хвилі Британського Хеві-Металу. Група серйозно вплила на формування та
                        розвиток металу узагалом. Гурт випустив 16 студійних альбомів, які продались у кількості
                        приблизно 100 мільйонів копій по всьому світу. <br><br>
                        Засновником групи та автором більшості пісень є бас-гітарист Стів Харріс, техніка гри якого
                        надихала багатьох займатися цим інструментом. Стів обрав назву групи під впливом фільму “Людина
                        в залізній масці”, де фігурувало це знаряддя тортур Середньовіччя.
                        Перший постійний склад Iron Maiden, з яким було випущено перші два студійних альбома включав у
                        себе Стіва Харріса, Пола Ді Анно, Дейва Мюррея, Деніса Стретона та Клайва Барра. Ці альбоми,
                        “Iron Maiden” та “Killers” відрізнялися “панковістю” та зухвалістю. Але оскільки лідером гурту
                        був Харріс, який хотів йти у іншому напрямку, було прийняте рішення замінити вокаліста Ді Анно
                        на більш професіонального та артистичного Брюса Дікінсона. Разом із ним, Maiden вийшли на новий
                        рівень успіху, ставши культовою метал групою. До сьогоднішнього дня ці піонери металу дають
                        концерти та випускають альбоми.
                        Вплив Iron Maiden визнавали багато інших легендарних груп, таких як Metallica, Megadeth, Slayer,
                        Anihillator та інші. <br><br>
